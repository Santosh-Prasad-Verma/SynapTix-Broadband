    private String formatSpeed(double mbps) {
        if (mbps >= 1000.0) {
            return String.format("%.2f Gbps", mbps / 1000.0);
        } else if (mbps >= 1.0) {
            return String.format("%.1f Mbps", mbps);
        } else {
            return String.format("%.1f Kbps", mbps * 1024.0);
        }
    }

    private long[] getNetworkBytes() {
        long rxBytes = 0;
        long txBytes = 0;
        try (java.io.BufferedReader reader = new java.io.BufferedReader(new java.io.FileReader("/proc/net/dev"))) {
            String line;
            reader.readLine(); // skip header 1
            reader.readLine(); // skip header 2
            while ((line = reader.readLine()) != null) {
                line = line.trim();
                String[] parts = line.split(":");
                if (parts.length < 2) continue;
                String iface = parts[0].trim();
                if (iface.equals("lo") || iface.startsWith("docker") || iface.startsWith("br-") || iface.startsWith("veth")) {
                    continue;
                }
                String[] stats = parts[1].trim().split("\\s+");
                if (stats.length >= 9) {
                    try {
                        rxBytes += Long.parseLong(stats[0]);
                        txBytes += Long.parseLong(stats[8]);
                    } catch (NumberFormatException e) {
                        // ignore
                    }
                }
            }
        } catch (Exception e) {
            return null;
        }
        return new long[]{rxBytes, txBytes};
    }

