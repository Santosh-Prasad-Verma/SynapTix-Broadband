    private void loadStatistics() {
        int activeCustomers = 0;
        double monthlyRevenue = 0.0;
        int openComplaints = 0;
        int solvedComplaints = 0;

        try {
            // 1. Total Customers
            String sqlCust = "SELECT COUNT(*) FROM customer";
            try (PreparedStatement pstCust = conn.prepareStatement(sqlCust);
                 ResultSet rsCust = pstCust.executeQuery()) {
                if (rsCust.next()) {
                    activeCustomers = rsCust.getInt(1);
                }
            }

            // 2. Total Monthly Revenue
            String sqlRev = "SELECT SUM(plan.Cost) FROM customer JOIN plan ON customer.Plan = plan.PlanName";
            try (PreparedStatement pstRev = conn.prepareStatement(sqlRev);
                 ResultSet rsRev = pstRev.executeQuery()) {
                if (rsRev.next()) {
                    monthlyRevenue = rsRev.getDouble(1);
                }
            }

            // 3. Complaints
            String sqlComp = "SELECT Action, COUNT(*) FROM complain GROUP BY Action";
            try (PreparedStatement pstComp = conn.prepareStatement(sqlComp);
                 ResultSet rsComp = pstComp.executeQuery()) {
                while (rsComp.next()) {
                    String action = rsComp.getString(1);
                    int count = rsComp.getInt(2);
                    if ("Solved".equalsIgnoreCase(action)) {
                        solvedComplaints += count;
                    } else {
                        openComplaints += count;
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        lblActiveCustomersVal.setText(String.valueOf(activeCustomers));
        lblRevenueVal.setText("Rs. " + String.format("%.2f", monthlyRevenue));
        lblComplaintsVal.setText(solvedComplaints + " / " + (openComplaints + solvedComplaints) + " Solved");
    }

