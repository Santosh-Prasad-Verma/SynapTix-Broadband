    private void startMonitorTimer(final org.jfree.chart.JFreeChart chart) {
        if (monitorTimer != null) {
            monitorTimer.stop();
        }

        monitorTimer = new javax.swing.Timer(1000, new java.awt.event.ActionListener() {
            private final java.util.Random rand = new java.util.Random();

            @Override
            public void actionPerformed(java.awt.event.ActionEvent e) {
                long[] currentBytes = getNetworkBytes();
                double dlMbps = 0.0;
                double ulMbps = 0.0;
                boolean isReal = false;

                if (currentBytes != null && lastBytes != null) {
                    long rxDiff = currentBytes[0] - lastBytes[0];
                    long txDiff = currentBytes[1] - lastBytes[1];
                    dlMbps = (rxDiff * 8.0) / (1024.0 * 1024.0);
                    ulMbps = (txDiff * 8.0) / (1024.0 * 1024.0);
                    if (dlMbps < 0) dlMbps = 0;
                    if (ulMbps < 0) ulMbps = 0;
                    isReal = true;
                } else {
                    lastDlSim = Math.max(5.0, Math.min(150.0, lastDlSim + (rand.nextDouble() - 0.5) * 15.0));
                    lastUlSim = Math.max(2.0, Math.min(60.0, lastUlSim + (rand.nextDouble() - 0.5) * 6.0));
                    dlMbps = lastDlSim;
                    ulMbps = lastUlSim;
                }

                lastBytes = currentBytes;
                int ping = isReal ? (3 + rand.nextInt(5)) : (10 + rand.nextInt(15)); // real ping is faster!

                if (chart != null) {
                    chart.setTitle(isReal ? "ISP Real-time Traffic (Host Interface)" : "ISP Simulated Traffic (NOC Demo)");
                }

                org.jfree.data.time.Second currentSecond = new org.jfree.data.time.Second();
                if (downloadSeries != null) downloadSeries.addOrUpdate(currentSecond, dlMbps);
                if (uploadSeries != null) uploadSeries.addOrUpdate(currentSecond, ulMbps);

                if (lblLiveDl != null) lblLiveDl.setText(formatSpeed(dlMbps));
                if (lblLiveUl != null) lblLiveUl.setText(formatSpeed(ulMbps));
                if (lblLivePing != null) lblLivePing.setText(ping + " ms");
            }
        });
        monitorTimer.start();
    }

    private void stopMonitorTimer() {
        if (monitorTimer != null) {
            monitorTimer.stop();
            monitorTimer = null;
        }
    }

