    private void btnChangePwdActionPerformed(java.awt.event.ActionEvent evt) {
        javax.swing.JPanel panel = new javax.swing.JPanel(new java.awt.GridLayout(3, 2, 10, 10));
        javax.swing.JPasswordField txtCurrent = new javax.swing.JPasswordField();
        javax.swing.JPasswordField txtNew = new javax.swing.JPasswordField();
        javax.swing.JPasswordField txtConfirm = new javax.swing.JPasswordField();

        panel.add(new javax.swing.JLabel("Current Password:"));
        panel.add(txtCurrent);
        panel.add(new javax.swing.JLabel("New Password:"));
        panel.add(txtNew);
        panel.add(new javax.swing.JLabel("Confirm New Password:"));
        panel.add(txtConfirm);

        int result = javax.swing.JOptionPane.showConfirmDialog(this, panel, "Change Admin Password",
            javax.swing.JOptionPane.OK_CANCEL_OPTION, javax.swing.JOptionPane.PLAIN_MESSAGE);

        if (result == javax.swing.JOptionPane.OK_OPTION) {
            String currentPwd = new String(txtCurrent.getPassword());
            String newPwd = new String(txtNew.getPassword());
            String confirmPwd = new String(txtConfirm.getPassword());

            if (currentPwd.trim().isEmpty() || newPwd.trim().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Fields cannot be empty!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (!newPwd.equals(confirmPwd)) {
                javax.swing.JOptionPane.showMessageDialog(this, "New passwords do not match!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }

            try {
                String sql = "SELECT password FROM admin WHERE username='admin'";
                String storedHash = null;
                try (PreparedStatement pst = conn.prepareStatement(sql);
                     ResultSet rs = pst.executeQuery()) {
                    if (rs.next()) {
                        storedHash = rs.getString(1);
                    }
                }

                boolean auth = false;
                if (storedHash != null) {
                    if (storedHash.contains(":")) {
                        auth = PasswordHasher.checkPassword(currentPwd, storedHash);
                    } else {
                        auth = storedHash.equals(currentPwd);
                    }
                }

                if (!auth) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Incorrect current password!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                    return;
                }

                String newHash = PasswordHasher.hashPassword(newPwd);
                String updateSql = "UPDATE admin SET password=? WHERE username='admin'";
                try (PreparedStatement updatePst = conn.prepareStatement(updateSql)) {
                    updatePst.setString(1, newHash);
                    updatePst.executeUpdate();
                }

                javax.swing.JOptionPane.showMessageDialog(this, "Admin password updated successfully!");

            } catch (Exception ex) {
                ex.printStackTrace();
                javax.swing.JOptionPane.showMessageDialog(this, "Error changing password: " + ex.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
    }

